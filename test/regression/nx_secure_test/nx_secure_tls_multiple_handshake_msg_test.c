static void ntest_0_entry(ULONG thread_input)
{
UINT i;
UINT status;
ULONG response_length;
NX_PACKET *send_packet;
NX_PACKET *receive_packet;
ULONG      bytes_copied;

    /* Print out test information banner.  */
    printf("NetX Secure Test:   TLS Multiple Handshake Messages Test...............");

    /* Create TCP socket. */
    status = nx_tcp_socket_create(&ip_0, &server_socket_0, "Server socket", NX_IP_NORMAL,
                                  NX_DONT_FRAGMENT, NX_IP_TIME_TO_LIVE, 8192, NX_NULL, NX_NULL);
    if (status)
    {
        ERROR_COUNTER();
    }

    status = nx_tcp_server_socket_listen(&ip_0, SERVER_PORT, &server_socket_0, 5, NX_NULL);
    if (status)
    {
        ERROR_COUNTER();
    }

    for (i = 0; i < sizeof(testcases) / sizeof(TLS_HANDSHAKE_TESTCASE); i++)
    {

        /* Make sure client thread is ready. */
        tx_thread_suspend(&thread_0);


        status = nx_tcp_server_socket_accept(&server_socket_0, NX_WAIT_FOREVER);
        if (status)
        {
            ERROR_COUNTER();
        }

        /* Receive ClientHello. */
        status = nx_tcp_socket_receive(&server_socket_0, &receive_packet, NX_WAIT_FOREVER);
        EXPECT_EQ(NX_SUCCESS, status);

        /* Release the ClientHello packet. */
        nx_packet_release(receive_packet);

        status = nx_packet_allocate(&pool_0, &send_packet, NX_IPv4_TCP_PACKET, NX_WAIT_FOREVER);
        EXPECT_EQ(NX_SUCCESS, status);

        status = nx_packet_data_append(send_packet, serverhello_cert_serverhellodone, sizeof(serverhello_cert_serverhellodone), &pool_0, NX_WAIT_FOREVER);
        EXPECT_EQ(NX_SUCCESS, status);

        status = nx_tcp_socket_send(&server_socket_0, send_packet, NX_WAIT_FOREVER);
        EXPECT_EQ(NX_SUCCESS, status);

        /* Receive ClientKeyExchange or Alert. */
        status = nx_tcp_socket_receive(&server_socket_0, &receive_packet, NX_WAIT_FOREVER);
        EXPECT_EQ(NX_SUCCESS, status);

        nx_packet_data_extract_offset(receive_packet, 0, clientkeyexchange, sizeof(clientkeyexchange), &bytes_copied);
        EXPECT_EQ(NX_SUCCESS, status);

        EXPECT_TRUE(bytes_copied >= 7);

        if (testcases[i].expected_client_key_exchange)
        {
            /* Content Type: Handshake (22). */
            EXPECT_EQ(22, clientkeyexchange[0]);

            /* Handshake Type: Client Key Exchange (16). */
            EXPECT_EQ(16, clientkeyexchange[5]);
        }
        else
        {
            /* Content Type: Alert (21). */
            EXPECT_EQ(21, clientkeyexchange[0]);

            /* Length: 2. */
            EXPECT_EQ(0, clientkeyexchange[3]);
            EXPECT_EQ(2, clientkeyexchange[4]);

            /* Alert Level: Fatal (2). */
            EXPECT_EQ(NX_SECURE_TLS_ALERT_LEVEL_FATAL, clientkeyexchange[5]);
        }

        nx_tcp_socket_disconnect(&server_socket_0, NX_NO_WAIT);
        nx_tcp_server_socket_unaccept(&server_socket_0);
        nx_tcp_server_socket_relisten(&ip_0, SERVER_PORT, &server_socket_0);
    }


    /* Test server receiving multiple handshake messages */

    /* Make sure client thread is ready. */
    tx_thread_suspend(&thread_0);

    server_tls_setup(&tls_server_session_0);


    status = nx_tcp_server_socket_accept(&server_socket_0, NX_WAIT_FOREVER);
    if (status)
    {
        ERROR_COUNTER();
    }

    /* Start TLS session. */
    status = nx_secure_tls_session_start(&tls_server_session_0, &server_socket_0,
                                         NX_WAIT_FOREVER);

    nx_secure_tls_session_end(&tls_server_session_0, NX_NO_WAIT);
    nx_secure_tls_session_delete(&tls_server_session_0);

    nx_tcp_socket_disconnect(&server_socket_0, NX_NO_WAIT);
    nx_tcp_server_socket_unaccept(&server_socket_0);



    tx_thread_sleep(10);

    if (error_counter)
    {
        printf("ERROR!\n");
        test_control_return(1);
    }
    else
    {
        printf("SUCCESS!\n");
        test_control_return(0);
    }
}

