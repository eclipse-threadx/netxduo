    status = nx_secure_x509_crl_revocation_check(TestCA_crl, sizeof(TestCA_crl), store, &revoked_certificate);
    EXPECT_EQ(NX_CRYPTO_NOT_SUCCESSFUL, status);
    test_public_cipher_method.nx_crypto_cleanup = NX_NULL;

    status = nx_secure_x509_crl_revocation_check(TestCA_crl, sizeof(TestCA_crl), store, &revoked_certificate);
#ifdef NX_SECURE_KEY_CLEAR
    EXPECT_EQ(NX_SECURE_X509_PKCS7_PARSING_FAILED, status); /* Unable to decode PKCS7 when signature is all zero. */
#else
    EXPECT_EQ(NX_SECURE_X509_WRONG_SIGNATURE_METHOD, status);
#endif

    status = _nx_secure_x509_certificate_revocation_list_parse(TestCA_crl, sizeof(TestCA_crl), &crl_bytes, &crl);
    EXPECT_EQ(NX_SUCCESS, status);

    trusted_certificate.nx_secure_x509_public_algorithm = 0;
    status = _nx_secure_x509_crl_verify(&revoked_certificate, &crl, store, &trusted_certificate);
    EXPECT_EQ(NX_SECURE_X509_WRONG_SIGNATURE_METHOD, status);

    test_public_cipher_method.nx_crypto_algorithm = NX_CRYPTO_DIGITAL_SIGNATURE_ECDSA;
        
    status = _nx_secure_x509_crl_verify(&revoked_certificate, &crl, store, &trusted_certificate);
    EXPECT_EQ(NX_SECURE_X509_WRONG_SIGNATURE_METHOD, status);

    trusted_certificate.nx_secure_x509_public_algorithm = NX_SECURE_TLS_X509_TYPE_EC;
    status = _nx_secure_x509_crl_verify(&revoked_certificate, &crl, store, &trusted_certificate);
