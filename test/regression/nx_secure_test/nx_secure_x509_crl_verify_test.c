static UINT test_nx_crypto_method_md5_operation(UINT op,       /* Encrypt, Decrypt, Authenticate */
                                VOID *handler, /* Crypto handler */
                                struct NX_CRYPTO_METHOD_STRUCT *method,
                                UCHAR *key,
                                NX_CRYPTO_KEY_SIZE key_size_in_bits,
                                UCHAR *input,
                                ULONG input_length_in_byte,
                                UCHAR *iv_ptr,
                                UCHAR *output,
                                ULONG output_length_in_byte,
                                VOID *crypto_metadata,
                                ULONG crypto_metadata_size,
                                VOID *packet_ptr,
                                VOID (*nx_crypto_hw_process_callback)(VOID *packet_ptr, UINT status))
{
    UINT status;

    status = crypto_method_md5.nx_crypto_operation(op, handler, method, key, key_size_in_bits, input,
        input_length_in_byte, iv_ptr, output, output_length_in_byte, crypto_metadata,
        crypto_metadata_size, packet_ptr, nx_crypto_hw_process_callback);

    if((op == NX_CRYPTO_VERIFY) && output)
    {
        memset(output, 0, output_length_in_byte);
    }

    return status;
}

